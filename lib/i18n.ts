import type { AgentStance, Locale, MarketSnapshot, Verdict } from "./types";

export const locales: Record<Locale, { label: string; shortLabel: string }> = {
  en: { label: "English", shortLabel: "EN" },
  zh: { label: "中文", shortLabel: "中文" }
};

export function normalizeLocale(value: unknown): Locale {
  return value === "zh" ? "zh" : "en";
}

export function resolveLocale(lang: unknown, cookieLocale?: string | null): Locale {
  if (lang === "zh" || lang === "en") return lang;
  return cookieLocale === "zh" ? "zh" : "en";
}

export function trendLabel(trend: MarketSnapshot["trend"], locale: Locale) {
  const labels: Record<MarketSnapshot["trend"], Record<Locale, string>> = {
    up: { en: "Up", zh: "上行" },
    down: { en: "Down", zh: "下行" },
    flat: { en: "Range", zh: "震荡" }
  };
  return labels[trend][locale];
}

export function stanceLabel(stance: AgentStance, locale: Locale) {
  const labels: Record<AgentStance, Record<Locale, string>> = {
    bullish: { en: "Supports", zh: "支持" },
    bearish: { en: "Against", zh: "反对" },
    neutral: { en: "Neutral", zh: "中性" },
    "risk-off": { en: "Caution", zh: "谨慎" }
  };
  return labels[stance][locale];
}

export function verdictLabel(verdict: Verdict, locale: Locale) {
  const labels: Record<Verdict, Record<Locale, string>> = {
    allow: { en: "Clear", zh: "放行" },
    reduce: { en: "Reduce", zh: "减仓" },
    watch: { en: "Hold", zh: "观察" },
    block: { en: "Block", zh: "拦截" }
  };
  return labels[verdict][locale];
}

